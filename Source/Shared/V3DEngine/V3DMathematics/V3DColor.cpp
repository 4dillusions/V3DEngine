	V3DColor::V3DColor(const V3DVector3D& color)
	{
		r = color.x;
		g = color.y;
		b = color.z;
		a = 1.0f;
	}
	
	V3DColor::V3DColor(float r, float g, float b) : r{ r }, g{ g }, b{ b }
	{}
	
	V3DColor::V3DColor(float a, float r, float g, float b) : a{ a }, r{ r }, g{ g }, b{ b }
	{}

	V3DVector3D V3DColor::ConvertToFloatColorRGB() const
	{
		return V3DVector3D(1 / (255 / static_cast<float>(r)), 1 / (255 / static_cast<float>(g)), 1 / (255 / static_cast<float>(b)));
	}
